<?php

// While repete o bloco de código enquanto for verdadeiro ou 
// até satisfazer a sua condição.
// geralmente é necessário um contador 
// while (condicao){código}

    // A definição do contador
    $x = 0;

    // Início / Definição da estrutura
    while($x < 10) {

        // Corpo do Loop
        echo $x . "<br>";

        // Contador
        $x ++;
    }

    // A definição do contador
    $y = 0;

    // Início / Definição da estrutura
    while($y <= 10) {

        // Corpo do Loop
        echo $y . "<br>";

        // Contador
        $y = $y + 2;
    }

?>